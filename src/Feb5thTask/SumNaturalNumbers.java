package Feb5thTask;

// Print the sum of first 10 natural numbers using for
public class SumNaturalNumbers {
    public static void main(String[] args) {
        int Sum =0;
        for(int i=1;i<=10;i++){
            Sum+=i;
        }
        System.out.println("sum of first 10natural numbers:"+Sum);

    }
    }


